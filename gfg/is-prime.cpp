class Solution {
  public:
    bool isPrime(int n) {
        // code here
        if (n <= 1) return false;
        for(int i = 2; i <= sqrt(n); i++){
            if (n % i == 0) return false;
        }
        return true;
    }
};

int main() {
    int t;
    cin >> t;
    while(t--){
        int n,k;
        cin >> n >> k;
        vector<long long> a(n);
        for(int i = 0; i < n; i++) cin >> a[i];
        vector<long long> copy_a = a;
        sort(copy_a.begin(), copy_a.end());
        if (copy_a == a || k > 1) cout << "YES" << endl;
        else cout << "NO" << endl;
    }
    return 0;
}
